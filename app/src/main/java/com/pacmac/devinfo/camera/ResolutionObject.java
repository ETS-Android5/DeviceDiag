package com.pacmac.devinfo.camera;

class ResolutionObject {

    private final int width;
    private final int height;

    public ResolutionObject(int width, int height) {
        this.width = width;
        this.height = height;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }
}
